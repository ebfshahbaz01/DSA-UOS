#include <iostream>
using namespace std;
int main(){
	double num1,num2;
	cout<<"Enter 1st Number: ";
	cin>>num1;
	cout<<"Enter 2nd Number: ";
	cin>>num2;
	cout <<"Addition of "<<num1<<" and "<<num2<<" is: "<<num1+num2<<endl;
	cout <<"Multiplication of "<<num1<<" and "<<num2<<" is: "<<num1*num2<<endl;
	cout <<"Subtract of "<<num1<<" and "<<num2<<" is: "<<num1-num2<<endl;
	cout <<"Divion of "<<num1<<" and "<<num2<<" is: "<<num1/num2<<endl;
	return 0;
	
}
